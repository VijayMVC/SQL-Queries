SELECT  PT.TASK_ID AS PARENT_TASK_ID
       ,PT.TASK_NAME AS PARENT_TASK_NAME
       ,T.TASK_ID AS CHILD_TASK_ID
       ,T.TASK_NAME AS CHILD_TASK_NAME
FROM K12INTEL_METADATA.WORKFLOW_DEPENDENCY D
    ,K12INTEL_METADATA.WORKFLOW_TASK T
    ,K12INTEL_METADATA.WORKFLOW_TASK PT
    ,K12INTEL_METADATA.WORKFLOW_PACKAGE P
    ,K12INTEL_METADATA.WORKFLOW_PACKAGE P2
WHERE (D.TASK_UUID_CHILD=T.TASK_UUID)
  AND (D.TASK_UUID_PARENT =PT.TASK_UUID)
  AND (P.PACKAGE_UUID=T.PACKAGE_UUID AND P.PACKAGE_ID=1)
  AND (P2.PACKAGE_UUID=PT.PACKAGE_UUID AND P2.PACKAGE_ID=1)
AND PT.TASK_ID IN (10003)  -- 10003:BUILD MPS_IC D_SCHOOLS
ORDER BY 4
;
